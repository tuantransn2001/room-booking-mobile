const ErrorScreen = ({ statusCode }: ErrorScreenProps) => {
  const [dataError, setDataError] = React.useState({
    title: "",
    text: "",
    image: <></>,
    width: "",
  });

  React.useEffect(() => {
    switch (statusCode) {
      case 500: {
        setDataError({
          title: "SERVER REQUEST ERROR",
          text: "An error occurred while connecting to the server, please contact the Website administrator for more information",
          image: <ServerError />,
          width: "60%",
        });
        break;
      }
      case 404: {
        setDataError({
          title: "PAGE NOT FOUND",
          text: "The page you are visiting does not exist or has been permanently moved to a new page",
          image: <ServerError />,
          width: "60%",
        });
        break;
      }
      case 403: {
        setDataError({
          title: "ACCESS IS LIMITED",
          text: "You do not have permission to access this page, please contact the Website administrator for more information",
          image: <ServerError />,
          width: "65%",
        });
        break;
      }
      case 503: {
        setDataError({
          title: "SYSTEM MAINTENANCE",
          text: "The page you are visiting is being repaired and upgraded, please come back later",
          image: <ServerError />,
          width: "65%",
        });
        break;
      }
      default: {
        setDataError({
          title: "SERVER REQUEST ERROR",
          text: "An error occurred while connecting to the server, please contact the Website administrator for more information",
          image: <ServerError />,
          width: "60%",
        });
      }
    }
  }, [statusCode]);
  return (
    <View
      style={{
        flex: 1,
        justifyContent: "center",
        alignItems: "center",
      }}
    >
      {dataError.image}
      <View
        style={{
          marginHorizontal: 30,
          flexDirection: "column",
          gap: 12,
          justifyContent: "center",
          alignItems: "center",
        }}
      >
        <TextWrapper center>{dataError.title}</TextWrapper>
        <TextWrapper center>{dataError.text}</TextWrapper>
      </View>
    </View>
  );
};
export default ErrorScreen;
